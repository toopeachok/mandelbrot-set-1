package utils;

public class CartesianPlane {
  public final float xMax;
  public final float xMin;
  public final float yMax;
  public final float yMin;

  public CartesianPlane(float xMax, float xMin, float yMax, float yMin) {
    this.xMax = xMax;
    this.xMin = xMin;
    this.yMax = yMax;
    this.yMin = yMin;
  }

}
